/**
 * fbcon_font_glyph() - Return a tiny bitmap glyph for console text.
 * @ch: ASCII character requested by the framebuffer console.
 *
 * The boot console keeps the font deliberately small: unsupported characters
 * fall back to question mark.
 */
const uint8_t *fbcon_font_glyph(char ch)
{
	uint32_t index;

	for (index = 0; index < sizeof(font) / sizeof(font[0]); index++) {
		if (font[index].ch == ch) {
			return font[index].rows;
		}
	}

	return font[sizeof(font) / sizeof(font[0]) - 1].rows;
}
